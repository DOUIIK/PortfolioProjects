Select SUM(CONVERT(INT,new_cases)) as total_cases, SUM(CONVERT(INT,new_deaths)) as total_deaths, SUM(CONVERT(INT,new_deaths))/SUM(CONVERT(INT,New_Cases))*100 as DeathPercentage
From PortfolioProject..['owid-covid-data$']
--Where location like '%states%'
where continent is not null and( ISNUMERIC(new_cases) = 0 OR ISNUMERIC(new_deaths) = 0)
--Group By date
order by 1,2


BEGIN TRY
    SELECT 
        SUM(CONVERT(INT, CASE WHEN ISNUMERIC(new_cases) = 1 THEN new_cases ELSE '0' END)) as total_cases,
        SUM(CONVERT(INT, CASE WHEN ISNUMERIC(new_deaths) = 1 THEN new_deaths ELSE '0' END)) as total_deaths,
        CASE
            WHEN SUM(CONVERT(INT, CASE WHEN ISNUMERIC(new_cases) = 1 THEN new_cases ELSE '0' END)) = 0 THEN 0
            ELSE (SUM(CONVERT(INT, CASE WHEN ISNUMERIC(new_deaths) = 1 THEN new_deaths ELSE '0' END)) * 100.0) /
                 SUM(CONVERT(INT, CASE WHEN ISNUMERIC(new_cases) = 1 THEN new_cases ELSE '0' END))
        END as DeathPercentage
    FROM PortfolioProject..['owid-covid-data$']
    WHERE continent IS NOT NULL
    ORDER BY 1, 2;
END TRY
